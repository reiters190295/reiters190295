package at.reiters190295.bsp4;

public class DoubleNumber
{
    private Number first;
    private Number second;

    public DoubleNumber(Number first, Number second) {
        this.first = first;
        this.second = second;
    }

    public Number getFirst() {
        return first;
    }

    public Number getSecond() {
        return second;
    }
}
